#!/usr/bin/env node

import { createHash } from "node:crypto";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { inflateSync } from "node:zlib";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const DATA_DIR = join(ROOT, "packages", "game-data", "src");

const GAME_ORIGIN = "https://stonegy-online.com";
const ASSETS_ORIGIN = "https://assets.stonegy-online.com";

const HUNTS_MARKER = "let s=new Map";
const QUESTS_MARKER = "let np=new Map";
const BOSSES_MARKER = 'bossType:"Archfoe"';

const DATASET_TYPES = {
  items: 1,
  monsters: 2,
  mapItems: 3,
  weaponMasteryCatalog: 4,
};

async function fetchText(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url} (${response.status} ${response.statusText})`);
  }
  return response.text();
}

async function fetchBuffer(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url} (${response.status} ${response.statusText})`);
  }
  return Buffer.from(await response.arrayBuffer());
}

async function resolveAppBundleUrl() {
  const html = await fetchText(`${GAME_ORIGIN}/`);
  const match = html.match(/\/_next\/static\/chunks\/pages\/_app-[a-f0-9]+\.js/);
  if (!match) {
    throw new Error("Could not find the Stonegy _app bundle URL in the homepage HTML.");
  }
  return new URL(match[0], GAME_ORIGIN).toString();
}

function extractMapCall(source, marker) {
  const markerIdx = source.indexOf(marker);
  if (markerIdx < 0) {
    throw new Error(`Could not find marker in app bundle: ${marker}`);
  }

  const start = source.indexOf("new Map(", markerIdx);
  if (start < 0) {
    throw new Error(`Could not find Map(...) expression for marker: ${marker}`);
  }

  let i = source.indexOf("(", start);
  let depth = 0;
  let state = "code";
  let quote = "";

  while (i < source.length) {
    const ch = source[i];

    if (state === "code") {
      if (ch === '"' || ch === "'") {
        state = "string";
        quote = ch;
      } else if (ch === "(") {
        depth += 1;
      } else if (ch === ")") {
        depth -= 1;
        if (depth === 0) {
          return source.slice(start, i + 1);
        }
      }
    } else if (state === "string") {
      if (ch === "\\") {
        i += 2;
        continue;
      }
      if (ch === quote) {
        state = "code";
      }
    }

    i += 1;
  }

  throw new Error(`Unbalanced parentheses while extracting Map for marker: ${marker}`);
}

function extractNearbyConsts(source, marker) {
  const idx = source.indexOf(marker);
  const chunk = source.slice(Math.max(0, idx - 3000), idx);
  const decls = {};

  for (const match of chunk.matchAll(
    /\b([a-zA-Z_$][\w$]*)\s*=\s*("[^"\\]*(?:\\.[^"\\]*)*"|'[^'\\]*(?:\\.[^'\\]*)*'|\d+)/g
  )) {
    decls[match[1]] = match[2];
  }

  return decls;
}

function evaluateMapExpression(expression, consts = {}) {
  const constLines = Object.entries(consts)
    .map(([name, value]) => `const ${name} = ${value};`)
    .join("\n");

  return new Function(`${constLines}\nreturn ${expression}`)();
}

function toSortedValues(map) {
  return [...map.values()].sort((a, b) => a.id - b.id);
}

/** Boss catalog is `let o=[[id, record],...];a=new Map(o)` (not `new Map([[...]])`). */
function extractBosses(source) {
  const markerIdx = source.indexOf(BOSSES_MARKER);
  if (markerIdx < 0) {
    throw new Error(`Could not find bosses marker in app bundle: ${BOSSES_MARKER}`);
  }

  const arrayStart = source.lastIndexOf("[[", markerIdx);
  if (arrayStart < 0) {
    throw new Error("Could not find bosses array start near bossType marker.");
  }

  const endToken = "]],a=new Map(o)";
  const endIdx = source.indexOf(endToken, markerIdx);
  if (endIdx < 0) {
    throw new Error("Could not find bosses array end (]],a=new Map(o)).");
  }

  const expression = source.slice(arrayStart, endIdx + 2);
  const entries = new Function(`return ${expression}`)();
  return entries.map((entry) => entry[1]).sort((a, b) => a.id - b.id);
}

function formatTsValue(value, indent = 0) {
  return JSON.stringify(value, null, 2)
    .split("\n")
    .map((line, index) => (index === 0 ? line : `${" ".repeat(indent)}${line}`))
    .join("\n");
}

function buildGeneratedFile({ exportName, value, headerLines, exportType, typeImport, noCheck }) {
  const lines = [
    "// Auto-generated by scripts/generate-game-data.mjs",
    "// Do not edit manually. Run: bun run generate:game-data",
    ...headerLines,
    "",
  ];
  if (noCheck) {
    lines.push("// @ts-nocheck");
    lines.push("");
  }
  if (typeImport) {
    lines.push(`import type { ${typeImport} } from "@stonegy/helper/types";`);
    lines.push("");
  }
  const typeSuffix = exportType ? ` as unknown as ${exportType}` : "";
  lines.push(`export const ${exportName} = ${formatTsValue(value, 0)}${typeSuffix};`);
  lines.push("");
  return lines.join("\n");
}

async function fetchVersionInfo() {
  const [gameDataVersion, staticVersion] = await Promise.all([
    fetchText(`${ASSETS_ORIGIN}/game-data/version.json`).then(JSON.parse),
    fetchText(`${ASSETS_ORIGIN}/game-data/static/version.json`).then(JSON.parse),
  ]);

  return { gameDataVersion, staticVersion };
}

async function fetchStaticManifest(staticVersion) {
  const manifestUrl = new URL(staticVersion.manifest, `${ASSETS_ORIGIN}/`).toString();
  const manifest = await fetchText(manifestUrl).then(JSON.parse);

  if (manifest.format !== "stonegy-static-game-data-manifest-v1") {
    throw new Error("Unexpected static game-data manifest format.");
  }

  if (manifest.version !== staticVersion.version) {
    throw new Error("Static game-data manifest version mismatch.");
  }

  return manifest;
}

function decodeStonegyDataset(buffer, datasetName) {
  if (buffer.byteLength < 36) {
    throw new Error(`File .stonegy for ${datasetName} is smaller than the header.`);
  }

  const magic = buffer.subarray(0, 8).toString("ascii");
  if (magic !== "STGYDAT1") {
    throw new Error(`Invalid .stonegy magic for ${datasetName}: ${magic}`);
  }

  const view = new DataView(buffer.buffer, buffer.byteOffset, buffer.byteLength);
  const version = view.getUint16(8);
  const codec = view.getUint16(12);
  const datasetType = view.getUint16(14);
  const payloadLength = view.getUint32(16);
  const end = 36 + payloadLength;

  if (version !== 1) {
    throw new Error(`Invalid .stonegy version for ${datasetName}: ${version}`);
  }

  if (codec !== 1) {
    throw new Error(`Invalid .stonegy codec for ${datasetName}: ${codec}`);
  }

  if (end !== buffer.byteLength) {
    throw new Error(`Invalid .stonegy payload size for ${datasetName}.`);
  }

  if (datasetType !== DATASET_TYPES[datasetName]) {
    throw new Error(`Unexpected .stonegy dataset type for ${datasetName}: ${datasetType}`);
  }

  const expectedHash = buffer.subarray(20, 36);
  const payload = buffer.subarray(36, end);
  const actualHash = createHash("sha256").update(payload).digest().subarray(0, 16);

  if (!expectedHash.equals(actualHash)) {
    throw new Error(`Invalid .stonegy payload hash for ${datasetName}.`);
  }

  const decoded = inflateSync(payload);
  const records = JSON.parse(decoded.toString("utf8"));

  if (!Array.isArray(records)) {
    throw new Error(`Decoded ${datasetName} payload is not an array.`);
  }

  return records;
}

function prepareStonegyRecords(records) {
  return records
    .map((entry) => (Array.isArray(entry) ? entry[1] : entry))
    .filter((record) => record && typeof record === "object")
    .sort((a, b) => a.id - b.id);
}

async function fetchStaticDataset(staticVersion, datasetName) {
  const manifest = await fetchStaticManifest(staticVersion);
  console.log("manifest", manifest);
  const dataset = manifest.datasets[datasetName];

  if (!dataset?.url) {
    throw new Error(`Static game-data manifest is missing ${datasetName} dataset.`);
  }

  const url = new URL(dataset.url, `${ASSETS_ORIGIN}/`).toString();
  console.log(`Downloading ${datasetName} dataset: ${url}`);

  const buffer = await fetchBuffer(url);
  if (buffer.byteLength !== dataset.size) {
    throw new Error(
      `${datasetName} dataset size mismatch: expected ${dataset.size}, received ${buffer.byteLength}.`
    );
  }

  const fileHash = createHash("sha256").update(buffer).digest("hex");
  if (fileHash !== dataset.sha256) {
    throw new Error(`${datasetName} dataset SHA256 mismatch.`);
  }

  const records = decodeStonegyDataset(buffer, datasetName);
  if (records.length !== dataset.recordCount) {
    throw new Error(
      `${datasetName} dataset record count mismatch: expected ${dataset.recordCount}, received ${records.length}.`
    );
  }

  return {
    url,
    records: prepareStonegyRecords(records),
  };
}

async function fetchMonsters(staticVersion) {
  const { url, records } = await fetchStaticDataset(staticVersion, "monsters");

  return {
    url,
    monsters: records,
  };
}

async function fetchItems(staticVersion) {
  const { url, records } = await fetchStaticDataset(staticVersion, "items");

  return {
    url,
    items: records,
  };
}

function extractArrayExpression(source, marker, arrayStart) {
  const markerIdx = source.indexOf(marker);
  if (markerIdx < 0) {
    throw new Error(`Could not find battle catalog marker: ${marker}`);
  }

  const start = source.lastIndexOf(arrayStart, markerIdx);
  if (start < 0) {
    throw new Error(`Could not find array start "${arrayStart}" for marker: ${marker}`);
  }

  let i = source.indexOf("[", start);
  let depth = 0;
  let state = "code";
  let quote = "";

  while (i < source.length) {
    const ch = source[i];

    if (state === "code") {
      if (ch === '"' || ch === "'") {
        state = "string";
        quote = ch;
      } else if (ch === "[") {
        depth += 1;
      } else if (ch === "]") {
        depth -= 1;
        if (depth === 0) {
          return source.slice(start, i + 1);
        }
      }
    } else if (state === "string") {
      if (ch === "\\") {
        i += 2;
        continue;
      }
      if (ch === quote) {
        state = "code";
      }
    }

    i += 1;
  }

  throw new Error(`Unbalanced array while extracting marker: ${marker}`);
}

function pickFields(record, fields) {
  const picked = {};
  for (const field of fields) {
    if (record[field] !== undefined) {
      picked[field] = record[field];
    }
  }
  return picked;
}

function extractBattleOptions(bundle) {
  const healExpression = extractArrayExpression(
    bundle,
    'name:"Health Potion",levelMin:0',
    "[{name:"
  );
  const healConsts = extractNearbyConsts(bundle, 'name:"Health Potion",levelMin:0', 3000);
  const heals = evaluateMapExpression(healExpression, healConsts).map((entry) =>
    pickFields(entry, ["name", "levelMin", "vocation"])
  );

  const manaExpression = extractArrayExpression(
    bundle,
    'name:"Mana Potion",price:56',
    "[{name:"
  );
  const manaPotions = evaluateMapExpression(manaExpression).map((entry) => ({
    name: entry.name,
    levelMin: entry.level,
    vocation: entry.vocations,
  }));

  const arrowExpression = extractArrayExpression(bundle, 'name:"Onyx Arrow"', "[{atq:");
  const arrows = evaluateMapExpression(arrowExpression).map((entry) => ({
    name: entry.name,
    levelMin: entry.levelRequired,
    ammoKind: entry.ammoKind,
  }));

  const boltExpression = extractArrayExpression(bundle, 'name:"Power Bolt"', "[{atq:");
  const bolts = evaluateMapExpression(boltExpression).map((entry) => ({
    name: entry.name,
    levelMin: entry.levelRequired,
    ammoKind: entry.ammoKind,
  }));

  const spellPattern =
    /=\{name:"([^"]+)",vocation:"(DRUID|KNIGHT|PALADIN|SORCERER)",minLevel:(\d+),([^}]*)\}/g;
  const spells = [];
  const seenSpells = new Set();

  for (const match of bundle.matchAll(spellPattern)) {
    const name = match[1];
    const key = `${name}:${match[2]}`;
    if (seenSpells.has(key)) {
      continue;
    }
    seenSpells.add(key);

    const tail = match[4];
    spells.push({
      name,
      vocation: match[2],
      minLevel: Number(match[3]),
      minMagicLevel: Number(tail.match(/minMagicLevel:(\d+)/)?.[1] ?? 0),
      category: tail.match(/category:"([^"]+)"/)?.[1] ?? "ATTACK",
    });
  }

  spells.sort(
    (a, b) =>
      a.minLevel - b.minLevel || a.name.localeCompare(b.name) || a.vocation.localeCompare(b.vocation)
  );

  return { heals, manaPotions, arrows, bolts, spells };
}

function extractImbuements(bundle) {
  const expression = extractArrayExpression(bundle, 'id:"basic_scorch"', '[{id:"');
  const imbuements = evaluateMapExpression(expression).map((entry) => ({
    id: entry.id,
    name: entry.name,
    tier: entry.tier,
    category: entry.category,
    icon: entry.icon,
    description: entry.description,
    durationMs: entry.durationMs,
    goldFee: entry.goldFee,
    protectionGoldFee: entry.protectionGoldFee,
    successRate: entry.successRate,
    premiumOnly: !!entry.premiumOnly,
    ingredients: (entry.ingredients ?? []).map((ingredient) => ({
      itemName: ingredient.itemName,
      amount: ingredient.amount,
    })),
  }));

  imbuements.sort(
    (a, b) =>
      a.category.localeCompare(b.category) ||
      a.tier.localeCompare(b.tier) ||
      a.name.localeCompare(b.name)
  );

  return imbuements;
}

function defineCraftHelpers() {
  function aS(id, label, resultItemId, amount) {
    return {
      id,
      craftType: 2,
      action: "exchange",
      label,
      resultItemId,
      successChancePercent: 100,
      costs: [{ itemId: 299, amount }],
    };
  }

  function aC(id, label, resultItemId) {
    return {
      id,
      craftType: 4,
      action: "exchange",
      label,
      resultItemId,
      successChancePercent: 100,
      costs: [{ itemId: 578, amount: 50 }],
    };
  }

  function aM(id, label, goldTokenAmount, resultItems) {
    const primary = resultItems[0];
    return {
      id,
      craftType: 4,
      action: "exchange",
      label,
      resultItemId: primary.itemId,
      resultAmount: primary.amount,
      resultItems,
      successChancePercent: 100,
      costs: [{ itemId: 578, amount: goldTokenAmount }],
    };
  }

  return { aS, aC, aM };
}

function normalizeCraftCost(cost) {
  return {
    itemId: cost.itemId,
    amount: cost.amount,
  };
}

function normalizeCraftRecipe(entry) {
  const recipe = {
    id: entry.id,
    craftType: entry.craftType,
    action: entry.action,
    label: entry.label,
    successChancePercent: entry.successChancePercent,
    costs: (entry.costs ?? []).map(normalizeCraftCost),
  };

  if (entry.resultItemId !== undefined) {
    recipe.resultItemId = entry.resultItemId;
  }
  if (entry.resultAmount !== undefined) {
    recipe.resultAmount = entry.resultAmount;
  }
  if (entry.resultItems !== undefined) {
    recipe.resultItems = entry.resultItems.map(normalizeCraftCost);
  }
  if (entry.sourceItemId !== undefined) {
    recipe.sourceItemId = entry.sourceItemId;
  }
  if (entry.downgradeItemId !== undefined) {
    recipe.downgradeItemId = entry.downgradeItemId;
  }

  return recipe;
}

function extractCraft(bundle) {
  const umbralFamilies = evaluateMapExpression(
    extractArrayExpression(bundle, 'key:"blade",crudeItemId', "[{key:")
  );

  const assignment = "let aI=";
  const start = bundle.indexOf(assignment);
  const end = bundle.indexOf("];aI.map", start);
  if (start < 0 || end < 0) {
    throw new Error("Could not find NPC craft recipe catalog (aI) in app bundle.");
  }

  const expression = bundle.slice(start + assignment.length, end + 1);
  const { aS, aC, aM } = defineCraftHelpers();
  const recipes = new Function("aw", "aS", "aC", "aM", `return ${expression}`)(
    umbralFamilies,
    aS,
    aC,
    aM
  ).map(normalizeCraftRecipe);

  recipes.sort(
    (a, b) => a.craftType - b.craftType || a.id - b.id || a.label.localeCompare(b.label)
  );

  const typesExpression = extractArrayExpression(
    bundle,
    'craftType:1,label:"UMBRAL"',
    "[{craftType:"
  );
  const types = evaluateMapExpression(typesExpression).map((entry) => ({
    craftType: entry.craftType,
    label: entry.label,
  }));

  return {
    types,
    umbralFamilies: umbralFamilies.map((entry) => ({
      key: entry.key,
      crudeItemId: entry.crudeItemId,
      umbralItemId: entry.umbralItemId,
      masterItemId: entry.masterItemId,
    })),
    recipes,
  };
}

function extractEnchant(bundle) {
  const expression = extractArrayExpression(bundle, 'label:"Werewolf Amulet"', "[{id:");
  const enchants = evaluateMapExpression(expression).map((entry) => {
    const enchant = {
      id: entry.id,
      label: entry.label,
      sourceItemId: entry.sourceItemId,
      resultItemId: entry.resultItemId,
      costs: (entry.costs ?? []).map(normalizeCraftCost),
    };
    if (entry.goldCost !== undefined) {
      enchant.goldCost = entry.goldCost;
    }
    if (entry.blocksMarketResult !== undefined) {
      enchant.blocksMarketResult = !!entry.blocksMarketResult;
    }
    return enchant;
  });

  enchants.sort((a, b) => a.id - b.id || a.label.localeCompare(b.label));
  return enchants;
}

async function generateGameData() {
  const [bundleUrl, versions] = await Promise.all([
    resolveAppBundleUrl(),
    fetchVersionInfo(),
  ]);

  console.log(`Downloading app bundle: ${bundleUrl}`);
  const bundle = await fetchText(bundleUrl);

  const huntsExpression = extractMapCall(bundle, HUNTS_MARKER);
  const questsExpression = extractMapCall(bundle, QUESTS_MARKER);
  const huntsConsts = extractNearbyConsts(bundle, 'title:"Sewers"');

  const hunts = toSortedValues(evaluateMapExpression(huntsExpression, huntsConsts));
  const quests = toSortedValues(evaluateMapExpression(questsExpression));
  const bosses = extractBosses(bundle);
  const battleOptions = extractBattleOptions(bundle);
  const imbuements = extractImbuements(bundle);
  const craft = extractCraft(bundle);
  const enchant = extractEnchant(bundle);
  const { url: monstersUrl, monsters } = await fetchMonsters(versions.staticVersion);
  const { url: itemsUrl, items } = await fetchItems(versions.staticVersion);

  const generatedAt = new Date().toISOString();
  const sharedHeader = [
    `// Generated at: ${generatedAt}`,
    `// Game data version: ${versions.gameDataVersion.version}`,
    `// Static data version: ${versions.staticVersion.version}`,
  ];

  mkdirSync(DATA_DIR, { recursive: true });

  const files = [
    {
      path: join(DATA_DIR, "hunts.ts"),
      exportName: "HUNTS",
      value: hunts,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "quests.ts"),
      exportName: "QUESTS",
      value: quests,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "bosses.ts"),
      exportName: "BOSSES",
      value: bosses,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "monsters.ts"),
      exportName: "MONSTERS",
      value: monsters,
      headerLines: [...sharedHeader, `// Source dataset: ${monstersUrl}`],
    },
    {
      path: join(DATA_DIR, "items.ts"),
      exportName: "ITEMS",
      value: items,
      exportType: "ItemRecord[]",
      typeImport: "ItemRecord",
      noCheck: true,
      headerLines: [...sharedHeader, `// Source dataset: ${itemsUrl}`],
    },
    {
      path: join(DATA_DIR, "battle-options.ts"),
      exportName: "BATTLE_OPTIONS",
      value: battleOptions,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "imbuement.ts"),
      exportName: "IMBUEMENTS",
      value: imbuements,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "craft.ts"),
      exportName: "CRAFT",
      value: craft,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
    {
      path: join(DATA_DIR, "enchant.ts"),
      exportName: "ENCHANT",
      value: enchant,
      headerLines: [...sharedHeader, `// Source bundle: ${bundleUrl}`],
    },
  ];

  for (const file of files) {
    writeFileSync(file.path, buildGeneratedFile(file), "utf8");
    console.log(`Wrote ${file.path}`);
  }

  console.log(`  hunts: ${hunts.length}`);
  console.log(`  quests: ${quests.length}`);
  console.log(`  bosses: ${bosses.length}`);
  console.log(`  monsters: ${monsters.length}`);
  console.log(`  items: ${items.length}`);
  console.log(`  battle heals: ${battleOptions.heals.length}`);
  console.log(`  battle spells: ${battleOptions.spells.length}`);
  console.log(`  battle mana potions: ${battleOptions.manaPotions.length}`);
  console.log(`  battle arrows: ${battleOptions.arrows.length}`);
  console.log(`  battle bolts: ${battleOptions.bolts.length}`);
  console.log(`  imbuements: ${imbuements.length}`);
  console.log(`  craft recipes: ${craft.recipes.length}`);
  console.log(`  enchant recipes: ${enchant.length}`);
}

generateGameData().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
